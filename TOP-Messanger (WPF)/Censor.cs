        public static string Censoring(string message)
        {
            string[] messageWords = message.Split(' ');
            List<string> matsRusUpper = new List<string>();

            foreach (string word in mats_rus)
            {
                matsRusUpper.Add(word.ToUpperInvariant());
            }

            List<string> matsEngUpper = new List<string>();
            foreach (string word in mats_eng)
            {
                matsEngUpper.Add(word.ToUpperInvariant());
            }

            for (int i = 0; i < messageWords.Length; i++)
            {
                string wordUpper = messageWords[i].ToUpperInvariant();
                bool found = false;

                for (int j = 0; j < matsRusUpper.Count; j++)
                {
                    string mat = matsRusUpper[j];

                    if (wordUpper == mat)
                    {
                        messageWords[i] = "***";
                        found = true;
                        break;
                    }

                    // Проверяем мат внутри слова
                    if (mat.Length >= 3 && wordUpper.Contains(mat))
                    {
                        int index = wordUpper.IndexOf(mat);
                        string before = messageWords[i].Substring(0, index);
                        string after = messageWords[i].Substring(index + mat.Length);
                        messageWords[i] = before + "***" + after;
                        wordUpper = messageWords[i].ToUpperInvariant(); // Обновляем для дальнейших проверок
                        found = true;
                    }
                }

                // Проверяем английские маты, если русские не найдены
                if (!found)
                {
                    for (int j = 0; j < matsEngUpper.Count; j++)
                    {
                        string mat = matsEngUpper[j];

                        // Проверяем целое слово
                        if (wordUpper == mat)
                        {
                            messageWords[i] = "***";
                            break;
                        }

                        // Проверяем мат внутри слова
                        if (mat.Length >= 3 && wordUpper.Contains(mat))
                        {
                            int index = wordUpper.IndexOf(mat);
                            string before = messageWords[i].Substring(0, index);
                            string after = messageWords[i].Substring(index + mat.Length);
                            messageWords[i] = before + "***" + after;
                            break;
                        }
                    }
                }
            }

            // Собираем сообщение обратно
            string newMessage = "";
            for (int i = 0; i < messageWords.Length; i++)
            {
                newMessage += messageWords[i];
                if (i < messageWords.Length - 1)
                {
                    newMessage += " ";
                }
            }

            return newMessage;
        }
